-- ==========================================
-- שלב ב: ניהול עסקאות - Rollback ו-Commit
-- ==========================================

-- ---------------------------------------------------------
-- חלק 1: הדגמת ROLLBACK (ביטול פעולה)
-- ---------------------------------------------------------

-- 1. התחלת עסקה
START TRANSACTION;

-- 2. ביצוע מחיקה "בטעות" (מחיקת כל צוות העובדים)
DELETE FROM STAFF;

-- 3. בדיקה שהטבלה אכן ריקה כרגע (בתוך העסקה)
SELECT * FROM STAFF;

-- 4. ביצוע ROLLBACK - החזרת המצב לקדמותו
ROLLBACK;

-- 5. בדיקה סופית שהנתונים חזרו
SELECT * FROM STAFF;


-- ---------------------------------------------------------
-- חלק 2: הדגמת COMMIT (שמירה סופית)
-- ---------------------------------------------------------

-- 1. התחלת עסקה חדשה
START TRANSACTION;

-- 2. עדכון נתון (למשל: עדכון מספר טלפון של ספק)
UPDATE VENDORS 
SET Contact_Person = 'Updated by AI' 
WHERE Vendor_ID = 301;

-- 3. שמירה סופית של השינוי בבסיס הנתונים
COMMIT;

-- 4. ניסיון לבצע ROLLBACK (לא אמור לעבוד אחרי COMMIT)
ROLLBACK;

-- 5. בדיקה שהעדכון נשאר למרות הניסיון לבטל
SELECT * FROM VENDORS WHERE Vendor_ID = 301;